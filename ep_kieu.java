public class ep_kieu {
    public static void main(String[] args) {
        // Ep kieu ngam dinh
        int a = 100;
        int b = 2;
        float c = a;
        float d = b;
        System.out.println("a: " + a + " b: " + b);
        System.out.println("c: " + c + " d: " + d);
        // Ep kieu tuong minh
        float e = 3.5f;
        float f = 9.5f;
        int g = (int) e;
        int h = (int) f;
        System.out.println("e: " + e + " f: " + f);
        System.out.println("g: " + g + " h: " + h);
    }

}
